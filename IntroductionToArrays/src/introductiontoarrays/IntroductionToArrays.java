package introductiontoarrays;

public class IntroductionToArrays {

    public static void main(String[] args) {
        int ar[] = new int[10];
        
        ar[0] = 87;
        ar[1] = 543;
        ar[9] = 65;
        
        System.out.println(ar[1]);  
        
        int ar2[] = {2,4,5,7,9}; //<- array initializer
        
        System.out.println(ar2[2]);
    }
}


